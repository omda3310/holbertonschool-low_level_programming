#include <stdio.h>
#include <stdlib.h>
#include "3-calc.h"
/**
 * op_add - sum of two integers
 * @a: variable1
 * @b: variable2
 * Return: sum
 */
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - substraction
 * @a: variable1
 * @b: variable2
 * Return: substraction
 */
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplication
 * @a: variable1
 * @b: variable2
 * Return: multiplication
 */
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - division
 * @a: variable1
 * @b: variable2
 * Return: division
 */
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - a % b
 * @a: variable1
 * @b: variable2
 * Return: a % b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
